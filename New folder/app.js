
var parg=document.getElementById('parg')


function btn_1(){
    if(parg.style.fontWeight=="800"){

        parg.style.fontWeight="400" 
    }else{
        parg.style.fontWeight="800" 


    }
}

function btn_2(){
    if (parg.style.fontStyle=="italic") {
        
        parg.style.fontStyle="normal"
    }else{
        parg.style.fontStyle="italic"
    }
    
}

